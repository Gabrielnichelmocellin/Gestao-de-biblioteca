   private boolean validarCampos() {
        if (txtCpfUsuario.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o CPF do usuário!");
            return false;
        }
        if (txtTituloLivro.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha o título do livro!");
            return false;
        }
        return true;
    }


    private void realizarEmprestimo() {
        String cpf = txtCpfUsuario.getText();
        String titulo = txtTituloLivro.getText();

        Usuario usuario = BibliotecaDAO.buscarUsuarioPorCPF(cpf);
        Livro livro = BibliotecaDAO.buscarLivroPorTitulo(titulo);

        if (usuario == null || livro == null) {
            JOptionPane.showMessageDialog(this, "Usuário ou livro não encontrado!");
            return;
        }

        if (!livro.isDisponivel()) {
            JOptionPane.showMessageDialog(this, "Livro já emprestado!");
            return;
        }

        BibliotecaDAO.registrarEmprestimo(usuario, livro);
        JOptionPane.showMessageDialog(this, "Empréstimo registrado!");
        this.dispose();
    }

